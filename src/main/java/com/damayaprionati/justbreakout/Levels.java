    public void makeRandomLevel(){
        int brick;

        Random rand = new Random();
        num = rand.nextInt(3);
        Log.d("test: ", "" + num);

        if(num == 0){
            for (int column = 0; column < 10; column++) {
                for (int row = 4; row < 13; row++) {
                    brick = rand.nextInt(4);
                    levelRandom[row][column] = brick;
                }
            }
        }
        else if(num == 1){
            for (int column = 0; column < 10; column++) {
                brick = rand.nextInt(4);
                for (int row = 4; row < 13; row++) {
                    levelRandom[row][column] = brick;
                }
            }
        }
        else{
            for (int row = 4; row < 13; row++){
                brick = rand.nextInt(4);
                for(int column = 0; column < 10; column++){
                    levelRandom[row][column] = brick;
                }
            }
        }
    }

